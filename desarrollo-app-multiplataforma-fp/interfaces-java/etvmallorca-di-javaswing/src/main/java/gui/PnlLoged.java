    //Funcion que carga los Alojamientos dentro de la lista.
    public void cargarAlojamientos() {
        ArrayList<Allotjament> alojamientos = data.getAllotjaments(padre.getUserLogged().getId());
        DefaultListModel<Allotjament> dflmo = new DefaultListModel<>();
        for (Allotjament alojamiento : alojamientos) {
            dflmo.addElement(alojamiento);
        }

        lstAlojamientos.setModel(dflmo);
        scrAlojamientos.setViewportView(lstAlojamientos);
    }

    //Evento que se activa cada vez que se cambia el alojamiento seleccionado.
    private void lstAlojamientosValueChanged(javax.swing.event.ListSelectionEvent evt) {
        
        //Metodo para anular la doble entrada al cambiar el valor de la lista de alojamientos.
        if (!evt.getValueIsAdjusting()) {
            if (lstAlojamientos.getSelectedValue() != null) {
                Allotjament detallesAllotjament = data.getAllotjament(lstAlojamientos.getSelectedValue().getId());
                this.add(btnMostrarDetalles);
                btnMostrarDetalles.setBounds(80, 340, 560, 50);
                btnMostrarDetalles.setText("Ver/Editar");
                this.repaint();

            } else {
                this.remove(btnMostrarDetalles);
                this.repaint();
            }
        }
    }
